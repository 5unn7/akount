/** Lookup a country by its ISO code */
export function getCountryByCode(code: string): CountryData | undefined {
  return COUNTRIES.find(c => c.code === code)
}

/** Get the default currency for a country code */
export function getCurrencyForCountry(countryCode: string): string {
  return getCountryByCode(countryCode)?.currency ?? 'USD'
}

/** Get unique currencies from all countries, sorted alphabetically */
export function getAllCurrencies(): Array<{ code: string; name: string }> {
  const seen = new Map<string, string>()
  for (const c of COUNTRIES) {
    if (!seen.has(c.currency)) {
      seen.set(c.currency, c.currencyName)
    }
  }
  return Array.from(seen.entries())
    .map(([code, name]) => ({ code, name }))
    .sort((a, b) => a.code.localeCompare(b.code))
}